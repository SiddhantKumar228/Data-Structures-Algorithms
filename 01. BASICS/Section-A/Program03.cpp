// Size of Data Types

#include <iostream>
using namespace std;

int main()
{
    int a = 2;
    cout << sizeof(a) << endl;

    float b = 3.4;
    cout << sizeof(b) << endl;

    double d = 226663.23;
    cout << sizeof(d) << endl;

    char ch = 'a';
    cout << sizeof(ch) << endl;

    bool set = 1;
    cout << sizeof(set) << endl;

    return 0;
}